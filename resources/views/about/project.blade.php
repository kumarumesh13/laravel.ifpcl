                <h2>Project<span>Highlights</span></h2>
                <p>Ladakh is globally famous for its apricots. However, the  shelf life of the fruit is very short and the farmers are deprived of good  financial returns due to absence of any modern apricot fruit-processing  technologies adopted so far.  The farmers  continue with the age old sun drying of apricot fruits, which is not market  friendly as the quality of the fruit deteriorates considerably. The latest  Freeze drying technology to produce fully or semi dried fruits would retain  color nutrients taste etc. while the moisture content is reduced as per the  market requirement. Besides, various other products could be produced with the  latest processing technologies so that farmers get the maximum returns from  their produce.</p>


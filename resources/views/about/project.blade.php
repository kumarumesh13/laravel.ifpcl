                    <br/>
                    <p>         Besides the region, which is separated by 350 kms wide  Himalaya range, makes it an ideal place for seed production of various crops  like oilseeds, pulses, cereals and vegetables. There is a total isolation from  the main-land. Weeds pests and cross genes are kept at bay by this physical  barrier leading to the production of true to type seeds of various crops.</p>
                    <ul class="padding15">

                        <li>For the last 68 years, Ladakh has survived mainly on the  government programs, donations &amp; Charities.</li>
                        <li>With the result only the basic &amp; skeletal infrastructure has  been created.</li>
                        <li>The people at large continue to live a very harsh &amp;    difficult life in the remotest &amp; the toughest region of the   country.</li>
                        <li>The general poverty levels are very high and the employment  opportunities very limited.</li>
                        <li>The region has huge tracts of undeveloped / underdeveloped land.</li>
                        <li>Investment opportunities are almost non-existent.</li>
                        <li>Manufacturing is almost absent.</li>
                        <li>It is now high time that Ladakh gets integrated into the    national mainstream and ride the super highway of national development   &amp;  growth and attract sizeable investments in various sectors and   meet the growing  aspirations especially of the younger generations.</li>
                        <li>Road connectivity with the rest of the country available only  for about 5 -6 months.</li>
